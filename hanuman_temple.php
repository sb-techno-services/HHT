          <p>The founder and spiritual master Parama Pujya Dr.Sri Ganapathy Sachchidananda Swamiji (Sri Swamiji) made a sankalpa (statement of determination) to establish a Karya Siddhi Hanuman temple in London for the benefit of one and all in UK and Europe.</p>

<p>On behalf of Sri Datta Yoga Centre (U.K.), the Board of Trustees (BoT) appointed SGP builders to renovate and construct the historical church building in April 2015.</p>

<p>On 9th December 2015, during the celebration in Surat, India, Sri Swamiji approved the design of the main Karya Siddhi Hanuman temple structure and instructed BoT to construct the temple in traditional indian art and craft work using Burma Teak wood.</p>

<p>Board of Trustees appointed Maruti Handicraft, Ahmedabad, Gujarat State India to provide the main Karya Sidhhi Hanuman temple structure. Additionally, Rajasthani zarukha design was selected to compliment the main Hanuman temple and sculpted as per Vastu and Agama Shastra. All other relevant and necessary accompaniments, fittings and supporting structures were also prepared in India.</p>

<p>The Burma Teak wood was purchased in December 2015 from reputed and licenced timber yard. The construction and sculpting commenced in January 2016. All the sculpting was done by famed sculptures from Rajasthan and Bengal. At one time nearly 30 sculptures were working long hours to complete the structure by end of April 2016. The marathon task that normally takes 12 months was completed as planned in 4 months. On instructions of P.P. Sri Swamiji Prasad visited Ahmedabad and approved the temple. The temple was dismantled, all pieces appropriately labelled, packed and boxed ready to be shipped to UK following appropriate fumigation treatment.  Two containers 40 feet and 20 feet size were loaded from Mundra port, India and arrived and received at Felixstowe port, UK on 4th June 2016.  The BoT appointed Maine freights Ltd to clear customs and deliver at the centre.</p>

<p>The temple was assembled and completed by SGP builders on 19 July 2016 (Guru Purina) in readiness for the up coming Inauguration event scheduled by P.P. Sri Swamiji between 9th to 12th September 2016.</p>
